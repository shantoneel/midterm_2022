package collections.set;
import java.util.HashSet;
public class UseHashSet {
    public static void main(String[] args) {
HashSet<String> set = new HashSet<>();
        set.add("King");
        set.add("don");
        set.add("mike");
        set.add("Kelly");
        set.add("Henry");
        set.add("david");
        System.out.println(set);

    }
}
